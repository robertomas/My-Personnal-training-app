// Calcul de la phase courante en fonction de la date de début.
Phase getCurrentPhase(String? startDateISO, [DateTime? today]) {
  today ??= DateTime.now();
  if (startDateISO == null) return phases[0];
  final start = DateTime.parse(startDateISO);
  final diffDays = today.difference(start).inDays;
  final weekNum = (diffDays / 7).floor();
  if (weekNum < 8) return phases[0];
  if (weekNum < 16) return phases[1];
  return phases[2];
}

Session? getSessionForDay(Phase phase, String dayCode) =>
    phase.sessionForCode(dayCode);

/// Ordre séquentiel des 4 séances d'une semaine (UA -> LA -> UB -> LB).
const List<String> sessionOrder = ['UA', 'LA', 'UB', 'LB'];

List<Session> orderedSessions(Phase phase) {
  final out = <Session>[];
  for (final code in sessionOrder) {
    final s = phase.sessionForCode(code);
    if (s != null) out.add(s);
  }
  return out;
}

Phase? phaseById(String id) {
  for (final p in phases) {
    if (p.id == id) return p;
  }
  return null;
}

// Nom lisible d'un exercice à partir de son id (1re occurrence trouvée).
String exerciseName(String id) {
  for (final p in phases) {
    for (final s in p.sessions) {
      for (final ex in s.exercises) {
        if (ex.id == id) return ex.name;
      }
    }
  }
  return id;
}
